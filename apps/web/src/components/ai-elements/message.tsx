export type MessageResponseProps = ComponentProps<typeof Streamdown>;

const streamdownPlugins = { cjk, code, math, mermaid };

function LinkSafetyModal({ url, isOpen, onClose, onConfirm }: LinkSafetyModalProps) {
  let hostname: string;
  try {
    hostname = new URL(url).hostname;
  } catch {
    hostname = url;
  }

  return (
    <Dialog open={isOpen} onOpenChange={(open) => !open && onClose()}>
      <DialogContent showCloseButton={false}>
        <DialogHeader>
          <DialogTitle className="flex items-center gap-2">
            <ShieldAlertIcon className="size-4" />
            <T>External Link</T>
          </DialogTitle>
          <DialogDescription>
            <T>You&apos;re about to visit</T> <span className="font-medium">{hostname}</span>.{" "}
            <T>Are you sure you want to continue?</T>
          </DialogDescription>
        </DialogHeader>
        <div className="text-muted-foreground bg-muted rounded-md p-2 text-base break-all">
          {url}
        </div>
        <DialogFooter>
          <Button variant="outline" onClick={onClose}>
            <T>Cancel</T>
          </Button>
          <Button onClick={onConfirm}>
            <ExternalLinkIcon className="size-3.5" />
            <T>Open Link</T>
          </Button>
        </DialogFooter>
      </DialogContent>
    </Dialog>
  );
}

const linkSafetyConfig = {
  enabled: true,
  renderModal: (props: LinkSafetyModalProps) => <LinkSafetyModal {...props} />,
};

export const MessageResponse = memo(
  ({ className, ...props }: MessageResponseProps) => (
    <Streamdown
      className={cn("size-full [&>*:first-child]:mt-0 [&>*:last-child]:mb-0", className)}
      plugins={streamdownPlugins}
      linkSafety={linkSafetyConfig}
      {...props}
    />
  ),
  (prevProps, nextProps) => prevProps.children === nextProps.children,
);

MessageResponse.displayName = "MessageResponse";

